def to_mew x
  y = ""
  x.each_char do |c|
    c = c.ord
    if TRANS_MAP[c / 256] == 0
      raise "#{[c].pack 'U'} = 0x#{c.to_s 16}"
    elsif TRANS_MAP[c / 256] == 1
      y += "å–µ"
    else
      y += [c].pack 'U'
    end
  end
  y
end

while x = gets
  puts to_mew x
end
